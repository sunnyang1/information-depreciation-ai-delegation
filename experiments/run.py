#!/usr/bin/env python3
"""
Unified Experiment Runner

Usage:
    python run.py --list
    python run.py --experiment exp01
    python run.py --experiment all
    python run.py --category baseline
    python run.py --category v6_architecture,v6_frontloading

Options:
    -l, --list           List all registered experiments
    -e, --experiment     Experiment ID to run, or "all" (default: all)
    -c, --category       Category filter(s), comma-separated
    -o, --output         Output directory for results (default: ./results)
    --no-save            Do not write results to disk
"""

from __future__ import annotations

import argparse
import json
import sys
import traceback
from pathlib import Path
from typing import Dict, Any, List, Optional
from datetime import datetime

# Ensure the experiments package is on the path
PROJECT_ROOT = Path(__file__).parent.resolve()
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

# Import registry and trigger auto-registration of all experiments
import registry
import exps  # noqa: F401  # pylint: disable=unused-import


def _save_results(results: Dict[str, Any], output_dir: Path, timestamp: str):
    output_dir.mkdir(parents=True, exist_ok=True)
    out_path = output_dir / f"unified_results_{timestamp}.json"
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2, default=str)
    print(f"\n  Results saved to: {out_path}")


def _save_latex(results: Dict[str, Any], output_dir: Path, timestamp: str):
    """Generate a simple combined LaTeX snippet from all tabular results."""
    lines = []
    lines.append("% Combined LaTeX tables generated by run.py")
    lines.append(f"% Timestamp: {timestamp}")
    lines.append("")

    experiments = results.get("experiments", {})
    if not experiments:
        lines.append("% No experiments were run.")
    else:
        for exp_id, data in experiments.items():
            if not isinstance(data, dict):
                continue
            lines.append(f"% --- {exp_id} ---")
            status = data.get("status", "unknown")
            lines.append(f"% Status: {status}")
            result_data = data.get("data")
            if isinstance(result_data, dict):
                for key, val in result_data.items():
                    if isinstance(val, (int, float, str)):
                        lines.append(f"% {key}: {val}")
            lines.append("")

    tex_path = output_dir / f"unified_tables_{timestamp}.tex"
    with open(tex_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"  LaTeX stub saved to: {tex_path}")


def run_single(  # pylint: disable=unused-argument
    exp_id: str, output_dir: Path, save: bool
) -> Optional[Dict[str, Any]]:
    try:
        exp = registry.get_experiment(exp_id)
    except KeyError as e:
        print(f"\nError: {e}")
        return None

    print("\n" + "=" * 70)
    print(f"Running {exp.id}: {exp.name}")
    if exp.description:
        print(f"  {exp.description}")
    print("=" * 70)

    try:
        result = exp.run()
    except (RuntimeError, ValueError) as e:
        print(f"\n  FAILED: {type(e).__name__}: {e}")
        traceback.print_exc()
        return {"experiment_id": exp_id, "status": "failed", "error": str(e)}

    print(f"\n  Completed: {exp_id}")
    return {"experiment_id": exp_id, "status": "success", "data": result}


def run_all(
    output_dir: Path,
    save: bool,
    categories: Optional[List[str]] = None,
    exclude: Optional[List[str]] = None,
) -> Dict[str, Any]:
    all_ids = registry.get_all_ids()
    exclude = exclude or []

    to_run = []
    for exp_id in all_ids:
        if exp_id in exclude:
            continue
        exp = registry.get_experiment(exp_id)
        if categories and exp.category not in categories:
            continue
        to_run.append(exp_id)

    if not to_run:
        print("No experiments match the given filters.")
        return {}

    print(f"\nWill run {len(to_run)} experiment(s): {', '.join(to_run)}")

    results: Dict[str, Any] = {}
    failures = []
    for exp_id in to_run:
        res = run_single(exp_id, output_dir, save=save)
        if res is None:
            failures.append(exp_id)
            continue
        results[exp_id] = res
        if res.get("status") == "failed":
            failures.append(exp_id)

    summary = {
        "total": len(to_run),
        "successful": len(to_run) - len(failures),
        "failed": len(failures),
        "failed_ids": failures,
    }

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    payload = {
        "timestamp": timestamp,
        "summary": summary,
        "experiments": results,
    }

    if save:
        _save_results(payload, output_dir, timestamp)
        _save_latex(payload, output_dir, timestamp)

    print("\n" + "=" * 70)
    print("SUMMARY")
    print("=" * 70)
    print(f"  Total:     {summary['total']}")
    print(f"  Success:   {summary['successful']}")
    print(f"  Failed:    {summary['failed']}")
    if failures:
        print(f"  Failed IDs: {', '.join(failures)}")
    print("=" * 70)

    return payload


def main():
    parser = argparse.ArgumentParser(
        description="Unified runner for all Information Depreciation experiments",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument("-l", "--list", action="store_true", help="List all registered experiments")
    parser.add_argument("-e", "--experiment", default="all", help="Experiment ID or 'all'")
    parser.add_argument("-c", "--category", help="Comma-separated category filter(s)")
    parser.add_argument("-o", "--output", default="results", help="Output directory")
    parser.add_argument("--no-save", action="store_true", help="Do not write results to disk")
    parser.add_argument("--exclude", help="Comma-separated experiment IDs to skip")

    args = parser.parse_args()

    output_dir = Path(args.output)
    save = not args.no_save

    if args.list:
        registry.print_registry()
        return

    categories = [c.strip() for c in args.category.split(",")] if args.category else None
    exclude = [c.strip() for c in args.exclude.split(",")] if args.exclude else None

    if args.experiment == "all":
        run_all(output_dir=output_dir, save=save, categories=categories, exclude=exclude)
    else:
        res = run_single(args.experiment, output_dir, save)
        if res and save:
            ts = datetime.now().strftime("%Y%m%d_%H%M%S")
            payload = {"timestamp": ts, "experiments": {args.experiment: res}}
            _save_results(payload, output_dir, ts)
            _save_latex(payload, output_dir, ts)


if __name__ == "__main__":
    main()
